# function that adds two numbers and prints their sum
def add_numbers(a, b):
    result = a + b
    print(f"The sum of {a} and {b} is {result}")

# Using the function with integer values
add_numbers(5, 7)

# Using the function with float values
add_numbers(5.5, 7.3)

# Using the function with string values
add_numbers("Hello, ", "world!")

# We can see that the function works with different data types 
# and that the result is different depending on the data inputs.

